    public function index()
    {
        $posts = Posts::all();
        $settings = Settings::all()->first();
        $social_links = json_decode($settings->social_links);

        foreach ($posts as $post){
           $user = User::where('id', $post->user_id)->first()->name;
           $post->user_name = $user;
        }

        return view('front.home')->with(compact('posts', 'settings', 'social_links'));
    }

